    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String adminID = adminIDText.getText();
        String adminPassword = String.valueOf(adminPasswordField.getPassword());
        
        Boolean loginSuccessful = false;
        
        for (Admin admin: adminList){
            if (admin.ID.equals(adminID) && admin.password.equals(adminPassword)){
                loginSuccessful = true;
            }
        }
        
        if (loginSuccessful.equals(true)){
            new adminFunctions(adminID).setVisible(true);
            dispose();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Incorrect admin ID or Password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

