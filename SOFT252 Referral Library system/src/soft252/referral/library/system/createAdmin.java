    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String adminID = adminUsernameField.getText();
        String password = String.valueOf(adminPasswordField.getPassword());
        
        Boolean creationSuccessful = true;
        
        for (Admin admin: adminList){
            if (admin.ID.equals(adminID)){
                creationSuccessful = false;
            }
        }
        
        if (creationSuccessful.equals(true)){
            accountCreator.createAdmin(adminID, password);
            JOptionPane.showMessageDialog(rootPane, "New admin successfully created");
            dispose();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Username already in use, please try again");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

